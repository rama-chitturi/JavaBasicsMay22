package basics;

public class Operators {

	public static void main(String[] args) {
		
		/* Unary operators 
		 * Postfix  i++
		 * Prefix	--i
		 */
		int i=0, j=0;
		
		System.out.println("postfix: "+ i++);
		System.out.println("postfix: "+ i);
		System.out.println("prefix: "+ ++j);
		System.out.println("prefix: "+ j);
			
	}

}
